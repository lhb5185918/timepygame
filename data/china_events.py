def get_events_by_year(year):
    """根据年份获取事件列表"""
    year_str = f"{year}年"
    if year_str in CHINA_EVENTS:
        return CHINA_EVENTS[year_str]
    return []


def get_events_by_period(start_year, end_year):
    """获取指定时期的历史事件"""
    events = []
    for year in range(start_year, end_year + 1):
        year_events = get_events_by_year(year)
        if year_events:
            events.append({"year": year, "events": year_events})
    return events


def get_all_events():
    """获取所有历史事件"""
    return CHINA_EVENTS
